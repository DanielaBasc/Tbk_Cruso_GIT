package tipoDeDatos;

public class BooleanTestExercise {
	public static void main(String[] args){
		
		//Declaración de variable
		boolean testResult;
		
		//Inicialización de variable
		testResult = true;
		
		//Mostrar valor 
		System.out.println("El resultado de testResult es: " + testResult);
		
		//Cambiar el valor de la variable 
		testResult = false;
		
		//Mostrar nuevo valor 
		System.out.println("El resultado de testResult es: " + testResult);
	}
}
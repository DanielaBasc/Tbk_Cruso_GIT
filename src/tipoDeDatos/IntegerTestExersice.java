package tipoDeDatos;

public class IntegerTestExersice {

	public static void main(String[] args) {
		//Declaración de variable int
        int carSpeed;

        //Inicialización de variable con valor 20
        carSpeed = 20;

		// Imprime el valor de la variable char
		System.out.println("Car is running at the speed of: " +  carSpeed);

		//Cambiar el valor de la variable 
		carSpeed = carSpeed + 20;

		// Imprime el valor de la variable entera
		System.out.println("Current speed of the car is: " +  carSpeed);

	}

}